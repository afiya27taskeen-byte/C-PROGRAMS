#include <stdio.h>

int main() {
    int a,b;
    int sum, difference, multiplication , division;
    printf("Enter any two integers\n");
    scanf("%d %d" , &a,&b);
    sum=a+b;
    difference=a-b;
    multiplication=a*b;
    division=a/b;
    printf("sum,difference,multiplication and division of three integers: %d\n%d\n%d\n%d\n" , sum,difference,multiplication,division);

    return 0;
}












